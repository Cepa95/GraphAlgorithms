    def shortest(self, N, S, T, a, b):
        if (
            not (2 <= N <= 10**9)
            or not (1 <= S <= N)
            or not (1 <= T <= N)
            or S == T
            or len(a) != len(b)
            or len(a) > 1000
        ):
            return 0
        for i in a:
            if not (1 <= i <= N):
                return 0
        for i in b:
            if not (1 <= i <= N):
                return 0

        queue = [(S, 1)]
        visited = set([S])

        while queue:
            currentCity, distance = queue.pop(0)

            for i in range(len(a)):
                if currentCity % a[i] == 0:
                    for j in range(currentCity, N + 1, a[i]):
                        # if j % a[i] == 0:
                        for k in range(b[i], N + 1, b[i]):
                            if j != k and k not in visited:
                                if k == T:
                                    print(
                                        f"The shortest path from city {S} to city {T} is {distance} roads."
                                    )
                                    return distance
                                visited.add(k)
                                queue.append((k, distance + 1))
        print(f"There is no path from {S} to {T}")
        return -1


